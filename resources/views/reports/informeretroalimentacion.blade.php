          <h3>Proceso de Generación del Informe de Retroalimentación</h3>
          <p>
          El Informe de Retroalimentación se generó a través de todas las etapas del proceso de evaluación de las postulaciones
          al Reconocimiento a la Gestión de Proyectos de Mejora del Comité de Gestión de la Calidad, las cuales fueron:
          </p>
          <p><b>Selección Previa</b></p>
          <p>
          Los trabajos presentados son sometidos a una revisión documentaria por la Secretaría Técnica del Comité de Gestión de la Calidad,
          con el objeto de verificar que la organización postulante cumple con los requisitos establecidos en las bases.
          </p>
          <b>Evaluación por el Jurado Calificador</b>
        <br/>- Evaluación Individual
        <br/>Los equipos de evaluadores son conformados, atendiendo criterios de conocimientos, experiencias, habilidades y a la inexistencia de conflictos de interés.<br/>
        <p>Los evaluadores de manera individual y a través de la lectura y estudio detallado del trabajo de aplicación, proceden a elaborar el Cuaderno de Evaluación junto a los primeros hallazgos de fortalezas, áreas de mejora y temas para la visita en terreno.
        </p>
        <p>
          - Evaluación en Consenso
        </p>
        <p>
          Con el objeto de enriquecer la evaluación individual con los conocimientos y experiencias de los evaluadores,
          se busca construir una percepción de equipo, enriquecida con los aportes individuales, realizándose para ello diversas 
          reuniones de revisión y consenso. Luego de aclarar las diferencias de interpretación, se obtiene el Informe de Consenso. El Consejo
        Evaluador reúne los informes de consenso, correspondiente a todas las postulaciones objeto de evaluación,
        y define qué trabajos recibirán visitas en terreno.
        </p>
        <p>- Evaluación en Terreno</p>
        <p>En esta etapa se realizan visitas a las empresas con el propósito de verificar la información presentada en los trabajos.
        Durante la visita se llevan a cabo entrevistas con directivos, ejecutivos y responsables de la información contenida
        en el documento de postulación. Se tratan con prioridad los temas identificados como poco claros en el informe y los
        aspectos relacionados con la ejecución del proyecto de mejora.
        <p><b>Comité de Gestión de la Calidad</b></p>
        <p>Los hallazgos en la visita pueden aumentar o disminuir la evaluación realizada previamente y modificar en cierta medida
          las fortalezas y áreas de mejora identificadas en el Informe de Consenso.
        </p>
        <p>Luego de la visita, el equipo evaluador se reúne nuevamente y prepara el Informe de Visita en Terreno, documento que es revisado
          por la Secretaría Técnica para determinar si continúa hacia la fase final de evaluación.
        </p>
        <p><b>Sustentación ante el Consejo Evaluador</b></p>

        <p>El Consejo Evaluador está conformado por calificados profesionales, evaluadores senior y especialistas, analiza la postulación
          en sus aspectos esenciales y escucha la sustentación del equipo postulante. Además de los criterios técnicos,
          son considerados como determinantes el compromiso de la Alta Dirección de la empresa, la responsabilidad social y
          los valores éticos sobre los cuáles se construye la organización.
        </p>
