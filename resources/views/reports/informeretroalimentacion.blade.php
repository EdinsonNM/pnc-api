            <h3>Presentación</h3>
<p class="text-justify">


Un elemento clave de la cultura de la calidad es la gente: cuando trabajadores adecuadamente entrenados están involucrados en los problemas y retos de una organización participan en la toma de decisiones, trabajando en equipo, abordan sistemáticamente proyectos claves y logrando éxitos y crecimiento personal y organizacional.<br/><br/>

La importancia y trascendencia que tienen para el país los logros de los equipos de mejora y la necesidad de divulgarlos y estimular el incremento y aprovechamiento de su potencial llevó al Comité de Gestión de la Calidad a establecer el Reconocimiento a la Gestión de Proyectos de Mejora.<br/><br/>

El presente Informe de Retroalimentación es el mecanismo que utiliza el Comité de Gestión de la Calidad para dar a conocer a los participantes, información que puede ser usada por la empresa postulante como una base para la elaboración de planes de acción tendientes a la mejora. Esta información, presentada a través de comentarios de fortalezas y oportunidades de mejora, no constituye una evaluación exhaustiva, ni tiene carácter definitivo, ya que se sustenta únicamente en los resultados de la evaluación efectuada en el proceso del Reconocimiento y no en un estudio detallado de los procesos y documentación existente.<br/><br/>

Este trabajo es posible gracias a la participación desinteresada de los Evaluadores del Reconocimiento y a los representantes de su empresa, quienes con su aporte permitieron aplicar los criterios de evaluación de manera justa y objetiva.<br/>
